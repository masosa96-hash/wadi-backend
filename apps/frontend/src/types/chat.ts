// Types for chat functionality
export interface Message {
    id: string;
    conversation_id: string;
    role: 'user' | 'assistant';
    content: string;
    created_at: string;
}

export interface Conversation {
    id: string;
    user_id: string;
    title: string | null;
    message_count: number;
    created_at: string;
    updated_at: string;
}

export type WebSocketMessageType = 'start' | 'chunk' | 'complete' | 'error';

export interface WebSocketMessage {
    type: WebSocketMessageType;
    content?: string;
    message?: string;
}
